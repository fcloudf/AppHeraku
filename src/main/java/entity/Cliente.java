package entity;

/**
 *
 * @author fclou
 */
public class Cliente {
    
    private int codigo;
    private String nombre;
    private int edad;
    private String dni;
    private String correo;

    public Cliente() {
    }

    public Cliente(int codigo, String nombre, int edad, String dni, String correo) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.edad = edad;
        this.dni = dni;
        this.correo = correo;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }
    
    
    
}
